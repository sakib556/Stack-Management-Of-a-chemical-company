public void add(){
        String name=txtpName.getText();
        String spurchasePrice=txtpurchasePrice.getText();
        String ssellsPrice=txtsellsPrice.getText();
        long millis=System.currentTimeMillis();  
        java.sql.Date date2=new java.sql.Date(millis);  
         if(name.isEmpty()== true ||spurchasePrice.isEmpty()==true||ssellsPrice.isEmpty()==true){
            JOptionPane.showMessageDialog(this,"Please enter all records.");
        }
        else{ 
            try {
                pst=con.prepareStatement("insert into product_information (pname,purchasePrice,sellsPrice) values(?,?,?)");
                pst.setString(1,name);
                pst.setString(2,spurchasePrice);
                pst.setString(3,ssellsPrice);
                pst.executeUpdate(); 
                JOptionPane.showMessageDialog(this,"Data add success !!!!!");
                clear();
              
            } catch (SQLException ex) {
                Logger.getLogger(Employe_Information.class.getName()).log(Level.SEVERE, null, ex);
            }
          
        }}
