public void loadTable(){
        //dateBox();
        if(txtVoucharNo.getText().isEmpty()==false){
        try {
         DefaultTableModel dt=(DefaultTableModel)jTable1.getModel();
         dt.setRowCount(0);
            String svnumber="";
            svnumber=txtVoucharNo.getText();
            int vnumber=Integer.parseInt(svnumber);
            pst=con.prepareStatement("select *from product_purchase where vouchar_number=?");
            pst.setInt(1,vnumber);
            rs=pst.executeQuery();
        while(rs.next()){
         dt.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)
                                ,rs.getString(5),rs.getString(6),rs.getString(7)});
        }
        //  clear();
        } catch (SQLException ex) {
            Logger.getLogger(ProductPurchase.class.getName()).log(Level.SEVERE, null, ex);
        }}
    }
