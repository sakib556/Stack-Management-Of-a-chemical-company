public void showTable(){
        try {
            DefaultTableModel dt=(DefaultTableModel)jTable1.getModel();
            dt.setRowCount(0);
            String scustomer=customerNCombo.getSelectedItem().toString();
            pst=con.prepareStatement("SELECT *FROM `product_sales_collection` WHERE `customer_name`=?");
            pst.setString(1,scustomer);
            rs=pst.executeQuery();
            while(rs.next()){
                dt.addRow(new Object[]{rs.getString(2),rs.getString(3),rs.getString(4)
                        ,rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9),rs.getString(10)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductSalesCollection.class.getName()).log(Level.SEVERE, null, ex);
        }
}
public void select(){
      if(customerNCombo.getSelectedIndex()>=1){
             showTable();        
             calculate();   
         }
      else{
           DefaultTableModel dt=(DefaultTableModel)jTable1.getModel();
            dt.setRowCount(0);
             clear();
             oldAmount.setText("");
             
      }
}
