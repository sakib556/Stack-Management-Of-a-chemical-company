public void add(){
        String cname=txtCName.getText();
        String phone=txtPhoneNumber.getText();
        String address=txtAddress.getText();
        String details=txtDetails.getText();
        long millis=System.currentTimeMillis();  
        java.sql.Date date=new java.sql.Date(millis);  
         if(cname.isEmpty()== true ||phone.isEmpty()==true||address.isEmpty()==true||details.isEmpty()==true){
            JOptionPane.showMessageDialog(this,"Please enter all records.");
        }
        else{ 
            try {
                pst=con.prepareStatement("insert into supplier_information (company_name,address,phone_number,details,date) values(?,?,?,?,?)");
                pst.setString(1,cname);
                pst.setString(2,address);
                pst.setString(3,phone);
                pst.setString(4,details);
                pst.setDate(5,date);
                pst.executeUpdate(); 
                JOptionPane.showMessageDialog(this,"Data add success !!!!!");
                clear();
              
            } catch (SQLException ex) {
                Logger.getLogger(Supplier_information.class.getName()).log(Level.SEVERE, null, ex);
            }
          
        }}
