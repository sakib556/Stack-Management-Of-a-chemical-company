public void pprice(){
      if(productNCombo.getSelectedIndex()>0){        
        try {
            String proName= productNCombo.getSelectedItem().toString();
            pst=con.prepareStatement("SELECT purchasePrice,sellsPrice FROM product_information WHERE pName=?");
            pst.setString(1,proName);
            rs=pst.executeQuery();
           
            while(rs.next()){
                txtPurchasePrice.setText(rs.getString(1));
                txtSellsPrice.setText(rs.getString(2));
            }    
        } catch (SQLException ex) {
            Logger.getLogger(ProductPurchase.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
      else{
           txtPurchasePrice.setText("");
                txtSellsPrice.setText("");
      }
}
