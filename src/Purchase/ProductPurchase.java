public void loadWindow(){
     String sid="0";
        try {
            pst=con.prepareStatement("select *from product_purchase");
            rs=pst.executeQuery();
        while(rs.next()){
         sid=rs.getString(1);
        }
        int cid=Integer.parseInt(sid);
        cid=cid+1;
        sid=Integer.toString(cid);
        txtId.setText(sid);
        companyNCombo.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(ProductPurchase.class.getName()).log(Level.SEVERE, null, ex);
        }
}
