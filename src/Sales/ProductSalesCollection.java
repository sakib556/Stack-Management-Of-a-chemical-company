public void calculate(){
        try {
            pst=con.prepareStatement("SELECT  SUM(`total_price`) FROM `product_sales` WHERE `customer_name`=? GROUP BY `customer_name`");
            pst.setString(1,customerNCombo.getSelectedItem().toString());
            rs=pst.executeQuery();
            pst=con.prepareStatement("SELECT SUM(`paid`) FROM `product_sales_collection` WHERE `customer_name`=? GROUP BY `customer_name`");
            pst.setString(1,customerNCombo.getSelectedItem().toString());
            rs2=pst.executeQuery();
             int paid=0;
             int totalPur=0;
            while(rs.next()){
                 totalPur=Integer.parseInt(rs.getString(1));}
            while(rs2.next()){
                 paid =Integer.parseInt(rs2.getString(1));
            }
            oldAmounts=totalPur-paid;
            String totalPurs=Integer.toString(oldAmounts);
            oldAmount.setText(totalPurs);
            }
           catch (SQLException ex) {
            Logger.getLogger(ProductSalesCollection.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        }
