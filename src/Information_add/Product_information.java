public void load(){
        String sid="0";
        DefaultTableModel dt=(DefaultTableModel)jTable1.getModel();
        dt.setRowCount(0);
        try {
            pst=con.prepareStatement("select *from product_information");
            rs=pst.executeQuery();
        while(rs.next()){
         dt.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
         sid=rs.getString(1);
        }
        int cid=Integer.parseInt(sid);
        cid=cid+1;
        sid=Integer.toString(cid);
        txtId.setText(sid);
          clear();
         txtpName.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(Employe_Information.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
