public void load(){
        dateBox();
        String sid="0";
        DefaultTableModel dt=(DefaultTableModel)jTable1.getModel();
        dt.setRowCount(0);
        try {
            pst=con.prepareStatement("select *from supplier_information");
            rs=pst.executeQuery();
        while(rs.next()){
         dt.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)
                                ,rs.getString(5),rs.getString(6)});
         sid=rs.getString(1);
        }
        int cid=Integer.parseInt(sid);
        cid=cid+1;
        sid=Integer.toString(cid);
        txtId.setText(sid);
        //  clear();
         txtCName.requestFocus();
         clear();
        } catch (SQLException ex) {
            Logger.getLogger(Supplier_information.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
