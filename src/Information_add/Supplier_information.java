public void update(){
        String id=txtId.getText();
         int i=Integer.parseInt(id);
       String cname=txtCName.getText();
        String address=txtAddress.getText();
        String sphoneNumber=txtPhoneNumber.getText();
        String details=txtDetails.getText();
        long millis=System.currentTimeMillis();  
        java.sql.Date date2=new java.sql.Date(millis);  
         if(cname.isEmpty()== true || address.isEmpty()==true||sphoneNumber.isEmpty()==true||details.isEmpty()==true){
            JOptionPane.showMessageDialog(this,"Please enter all records.");
        }
        else{
         try {
             pst=con.prepareStatement("update supplier_information set company_name=?,address=?,phone_number=?,details=?,date=? where id=?");
            // pst.setInt(5,i);
             pst.setString(1,cname);
             pst.setString(2, address);
             int phoneNumber=Integer.parseInt(sphoneNumber);
             pst.setInt(3,phoneNumber);
             pst.setString(4,details);
             pst.setDate(5,date2);
             pst.setInt(6,i);
             pst.executeUpdate();
             JOptionPane.showMessageDialog(this,"Data update success !!!!!");
             clear();
             load();
             txtCName.requestFocus();
         } catch (SQLException ex) {
             Logger.getLogger(Supplier_information.class.getName()).log(Level.SEVERE, null, ex);
         }
}}
