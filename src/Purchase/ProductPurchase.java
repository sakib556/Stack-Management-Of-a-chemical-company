public void todayAmounts(){
        try {
            int vn=Integer.parseInt( txtVoucharNo.getText());
            pst=con.prepareStatement("SELECT SUM(total_price) FROM `product_purchase` WHERE `vouchar_number`=?\n" +
"GROUP BY vouchar_number");
            pst.setInt(1,vn);
            rs=pst.executeQuery();
            while(rs.next()){
                todayAmount.setText(rs.getString(1));
            }
            }
           catch (SQLException ex) {
            Logger.getLogger(ProductPurchase.class.getName()).log(Level.SEVERE, null, ex);
        } 
        }
public void oldAmountM(){
        try {
            pst=con.prepareStatement("SELECT  SUM(total_price) FROM `product_purchase` WHERE (`company_name`=?\n" +
                    "AND `vouchar_number`<> ?) GROUP BY `company_name`");
            pst.setString(1,companyNCombo.getSelectedItem().toString());
            pst.setString(2,txtVoucharNo.getText());
            rs2=pst.executeQuery();
            pst=con.prepareStatement("SELECT SUM(`paid`) FROM `purchase_due_paid` WHERE `company_name`=?\n" +
                    "GROUP BY `company_name`");
            pst.setString(1,companyNCombo.getSelectedItem().toString());
            rs3=pst.executeQuery();
            int paid=0;
            int totalPur=0;
            while(rs2.next()){
                totalPur=Integer.parseInt(rs2.getString(1));}
            while(rs3.next()){
                int paidtotal=Integer.parseInt(rs3.getString(1));
                paid=paid+paidtotal;
            }
            oldAmounts=totalPur-paid;
            String totalPurs=Integer.toString(oldAmounts);
            oldAmount.setText(totalPurs);
        } catch (SQLException ex) {
            Logger.getLogger(ProductPurchase.class.getName()).log(Level.SEVERE, null, ex);
        }
}
